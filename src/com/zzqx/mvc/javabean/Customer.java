package com.zzqx.mvc.javabean;

public class Customer {
	
	private Integer id;
	private String code;
	private String jsCode;
	private String name;
	private String jsName;
	private String type;
	private String address;
	private String bankAccount;
	private String cfPhone;
	private String bzPhone;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getJsCode() {
		return jsCode;
	}
	public void setJsCode(String jsCode) {
		this.jsCode = jsCode;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getJsName() {
		return jsName;
	}
	public void setJsName(String jsName) {
		this.jsName = jsName;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getBankAccount() {
		return bankAccount;
	}
	public void setBankAccount(String bankAccount) {
		this.bankAccount = bankAccount;
	}
	public String getCfPhone() {
		return cfPhone;
	}
	public void setCfPhone(String cfPhone) {
		this.cfPhone = cfPhone;
	}
	public String getBzPhone() {
		return bzPhone;
	}
	public void setBzPhone(String bzPhone) {
		this.bzPhone = bzPhone;
	}
}
